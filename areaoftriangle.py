#2 Script that prompts the user to enter base and height of a triangle and calculate the area of that triangle: The formular: 1/2base*height

base_of_triangle = float(input('Kindly enter the base of the triangle without the unit: '))
height_of_triangle = float(input('Kindly enter the height of the triangle without the unit: '))
area_of_triangle = 0.5 * base_of_triangle * height_of_triangle
print('The area of the triangle is: ', area_of_triangle, 'UNIT')

