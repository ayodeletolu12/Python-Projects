#2 The script below performs different operation on circle
# The script will request for user to enter the radius of a circle

radius_of_circle = float(input("Kindly enter the radius of the circle: "))
area_of_circle = 3.14 * radius_of_circle**2
circum_of_circle = 2 * 3.14 * radius_of_circle
print(f"The area of the circle with radius {radius_of_circle} is {area_of_circle} ")
print(f"The circumference of the circle with radius {radius_of_circle} is {circum_of_circle}")

